# Let's create a Movie class that will e used to display the current movie playing at a movie theater.

class Movie:
    name = "Toy Story 3"
    start_time = "4:45pm"
    end_time = "6:32pm"


movie = Movie()
print("Movie: " + movie.name)
print("Start: " + movie.start_time)
print("End: " + movie.end_time)
